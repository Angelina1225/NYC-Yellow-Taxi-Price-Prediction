from pyspark.sql import SparkSession
from pyspark.sql import functions as F
from pyspark.sql.types import *
from pyspark.sql.window import Window
import matplotlib.pyplot as plt

spark = SparkSession.builder.appName("NYC-Taxi-Models").getOrCreate()

final_df = spark.read.parquet("gs://colab-shared-bucket-files/final_df_parquet")

"""# Models and Evaluations"""

from pyspark.ml.feature import StringIndexer, OneHotEncoder

# Step 1: StringIndexer for conditions
conditions_indexer = StringIndexer(inputCol="conditions", outputCol="conditionsIndex")

# Transform the data
df_enc = conditions_indexer.fit(final_df).transform(final_df)

# Step 2: OneHotEncoder for conditions
conditions_encoder = OneHotEncoder(inputCol="conditionsIndex", outputCol="conditionsVec")

# Transform the data
df_enc = conditions_encoder.fit(df_enc).transform(df_enc)

# Verify the schema
df_enc.printSchema()

from pyspark.ml.feature import VectorAssembler

from pyspark.ml.regression import LinearRegression
from pyspark.ml.regression import RandomForestRegressor
from pyspark.ml.regression import GBTRegressor


from pyspark.ml.evaluation import RegressionEvaluator
import time

# Defined the predictor and target columns for the models
predictor_columns = [ 'passenger_count', 'trip_distance', 'trip_duration_sec', 'pickup_hour', 'dayofweek', 'isWeekend', 'year',
                     'month', 'day', 'minute', 'is_rush_hour', 'temp', 'humidity', 'precip', 'windspeed', 'conditionsVec', 'Latitude', 'Longitude']

target_column = "total_amount"

# Made sure there are no null values in the predictor columns
df_enc.select([
    F.count(F.when(F.col(c).isNull(), c)).alias(c)
    for c in predictor_columns
]).show(truncate=False)

# Vectorize predictors (combine all predictor columns into one feature vector)
vector_assembler = VectorAssembler(inputCols=predictor_columns, outputCol="features")
df_vectorized = vector_assembler.transform(df_enc)

# Models

# LINEAR REGRESSION MODEL
lr = LinearRegression(featuresCol="features", labelCol=target_column)

# RANDOM FOREST REGRESSION MODEL
rf = RandomForestRegressor(featuresCol="features", labelCol=target_column)

# HYPERTUNRD RANDOM FOREST REGRESSION MODEL
hypertuned_rf = RandomForestRegressor(
    featuresCol="features",
    labelCol=target_column,
    numTrees=50,
    maxDepth=6
)

# GRADIENT - BOOSTED REGRESSION MODEL
gbt = GBTRegressor(
    featuresCol="features",
    labelCol=target_column,
    maxIter=50,
    maxDepth=4,
    seed=42
)

# Defining evaluators - RMSE, MAE, R-square
evaluator_rmse = RegressionEvaluator(labelCol=target_column, predictionCol="prediction", metricName="rmse")
evaluator_mae = RegressionEvaluator(labelCol=target_column, predictionCol="prediction", metricName="mae")
evaluator_r2 = RegressionEvaluator(labelCol=target_column, predictionCol="prediction", metricName="r2")

# Defined a combine function to build the model and perform evaluations
# Helps to reduce repeating the same code multiple times

def model_build_evaluate(
    estimator, train_data, test_data, model_name
):
    # Training
    start_train = time.time()
    model = estimator.fit(train_data)
    training_time = time.time() - start_train

    # Testing / Prediction
    start_test = time.time()
    predictions = model.transform(test_data)
    testing_time = time.time() - start_test

    # Evaluation
    rmse = evaluator_rmse.evaluate(predictions)
    mae = evaluator_mae.evaluate(predictions)
    r2 = evaluator_r2.evaluate(predictions)
    throughput = predictions.count() / testing_time

    return {
        "model": model_name,
        "rmse": rmse,
        "mae": mae,
        "r2": r2,
        "training_time": training_time,
        "testing_time": testing_time,
        "throughput": throughput
    }

# Defined a function to display the results of the models
def model_results(results):
    print(f"{results['model']} RMSE: {results['rmse']}")
    print(f"{results['model']} MAE: {results['mae']}")
    print(f"{results['model']} R²: {results['r2']}")
    print(f"{results['model']} Training Time: {results['training_time']}")
    print(f"{results['model']} Testing Time: {results['testing_time']}")
    print(f"{results['model']} Throughput: {results['throughput']}")

