# Weather condition standardization:
# Multiple weather descriptors are reduced to one primary condition.

def pick_primary_condition(cond):
    if cond is None:
        return None
    parts = [p.strip() for p in cond.split(",")]

    if "Rain" in parts and "Snow" in parts:
        return "Sleet"

    elif "Rain" in parts:
        return "Rain"

    elif "Snow" in parts:
        return "Snow"

    else:
        return parts[0]

pick_condition_udf = F.udf(pick_primary_condition, StringType())

weather_clean = weather_clean.withColumn(
    "conditions",
    pick_condition_udf(F.col("conditions"))
)

weather_clean.select("conditions").distinct().show(truncate=False)

