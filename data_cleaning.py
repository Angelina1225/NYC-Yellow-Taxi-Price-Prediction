taxi_clean = taxi_clean.withColumn(
    "percentile_rank",
    F.percent_rank().over(w_rank)
)

taxi_clean = taxi_clean.withColumn(
    "is_rush_hour",
    (F.col("percentile_rank") <= 0.30).cast("int")
)

# Displaying the rush hours
